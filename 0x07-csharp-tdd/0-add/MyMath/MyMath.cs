namespace MyMath
{
	/// <summary> In this class the operations will be stored </summary>
	public class Operations
    {
        /// <summary> This function will make an addition </summary>
        public static int Add(int a, int b)
        {
            int x = a;
            int y = b;
            int z = x + y;
            return (z);
        }
    }
}